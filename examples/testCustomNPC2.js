var start = player.getLocation().add(2, 0, 2);

function spawnCrystals(){
	start = start.add(0, 0, 5);
	var loc = start;
	for(var n = 0; n<CustomNPC.Models.Crystals.length; n++){
		var npc = CustomNPC.createNPC(CustomNPC.Models.Crystals[n]);
		loc = loc.add(5, 0, 0);
		npc.spawn(loc);
	}
}

